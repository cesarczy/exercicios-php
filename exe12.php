exe12. Faça um algoritmo em PHP que dados dois valores A e B, apresente o resultado das 4 operações básicas da matemática: 
soma, subtração, divisão e multiplicação.

<br>
<br>
<br>
<br>

<?php
$valA = 4;
$valB = 2;
$resultado = "";

//subtração
$resultado = $valA - $valB;
echo "$valA - $valB é igual a $resultado";
echo "<br>";


//adição
$resultado = $valA + $valB;
echo "$valA + $valB é igual a $resultado";
echo "<br>";


//multiplicação
$resultado = $valA * $valB;
echo "$valA * $valB é igual a $resultado";
echo "<br>";


//divisão
$resultado = $valA / $valB;
echo "$valA / $valB é igual a $resultado";
echo "<br>";

?>